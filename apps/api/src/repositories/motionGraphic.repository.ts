/**
 * Repository for the AI Motion Graphic feature — raw `mysql2`, no ORM.
 *
 * Three tables (data-model.md + ADR-0008/0009/0010):
 *   - motion_graphics                 — the aggregate-root, code-as-TEXT graphic.
 *   - motion_graphic_chat_turns       — append-only, app-assigned `seq` chat history.
 *   - motion_graphic_block_snapshots  — the immutable frozen copy attached to a block.
 *
 * Plus the storyboard_block_media pivot (existing, extended by migration 061) for the
 * attach path.
 *
 * Design decisions:
 * - The application generates UUIDs (`randomUUID()` at the call site); the repo accepts ids.
 * - List is owner-scoped: WHERE user_id=? AND deleted_at IS NULL ORDER BY updated_at DESC
 *   (index idx_motion_graphics_user_active). Keyset cursor on (updated_at, id) DESC.
 * - Chat read uses idx_mg_chat_turns_graphic_seq: WHERE motion_graphic_id=? ORDER BY seq.
 * - append assigns the next monotonic seq via MAX(seq)+1 (0 for the first turn).
 * - The snapshot+block-media insert runs in one transaction (pool.getConnection +
 *   begin/commit/rollback) — both rows or neither (ADR-0009). The snapshot is a frozen
 *   COPY: nothing references the live graphic, so source edits never touch it (AC-10).
 * - NO authorization / NotFoundError / ready-state gating here — that is the service (T6).
 *
 * Pattern mirrors generation-flow.repository.ts / storyboard.repository.ts: pool.execute,
 * RowDataPacket row types, camelCase result mapping.
 */
import { randomUUID } from 'node:crypto';
import type { PoolConnection, ResultSetHeader, RowDataPacket } from 'mysql2/promise';
import { pool } from '@/db/connection.js';

